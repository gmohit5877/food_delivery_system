package dtos;

public class Restaurant {
    private Integer restaurnatID;
    private String name;
    private String area;
    private String city;
    private Integer rating;
    public Integer getRestaurnatID() {
        return restaurnatID;
    }
    public void setRestaurnatID(Integer restaurnatID) {
        this.restaurnatID = restaurnatID;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getArea() {
        return area;
    }
    public void setArea(String area) {
        this.area = area;
    }
    public String getCity() {
        return city;
    }
    public void setCity(String city) {
        this.city = city;
    }
    public Integer getRating() {
        return rating;
    }
    public void setRating(Integer rating) {
        this.rating = rating;
    }
 
    
    
    
}
